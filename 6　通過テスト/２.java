package exercise;

public class TKT6 {

	public static void main(String[] args) {
		// ここにコードを挿入
		// スタートと表示
		System.out.println("start");
		//m、nを変数化
		int m, n;
		//int i = 0; i < 3; i++を繰り返す
		for(int i = 0; i < 3; i++) {
			//整数を１つ入力しｍ,ｎに入れる
			m = 3;
			n = 2;
			//nをmで割った余りを表示する
			System.out.println(n%m);
		}
		//endと表示する
		System.out.println("end");

	}

}
